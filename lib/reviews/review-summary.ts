import { prisma } from "@/lib/db/prisma";

export type ReviewDecisionStatus = "PASS" | "REJECT";
export type ReviewStatusValue = ReviewDecisionStatus | "NONE";

export type ReviewSummary = {
    decision: ReviewDecisionStatus;
    comment: string;
    updatedAt: string;
    reviewerName: string;
};

export type ReviewSummaryGroup = {
    latestReview: ReviewSummary | null;
    aiReview: ReviewSummary | null;
    manualReview: ReviewSummary | null;
};

export type ReviewTarget = {
    projectId: string;
    datasourceId: string;
    externalRecordId: string;
};

export function buildReviewCompositeKey({
    projectId,
    datasourceId,
    externalRecordId,
}: ReviewTarget) {
    return `${projectId}::${datasourceId}::${externalRecordId}`;
}

export function isAiGeneratedReview(suggestions: unknown) {
    if (
        !suggestions ||
        typeof suggestions !== "object" ||
        Array.isArray(suggestions)
    ) {
        return false;
    }

    const source = (suggestions as Record<string, unknown>).source;
    const autoGenerated = (suggestions as Record<string, unknown>)
        .autoGenerated;

    return source === "AI_STRATEGY" || autoGenerated === true;
}

export function toReviewStatusValue(
    review: ReviewSummary | null,
): ReviewStatusValue {
    return review?.decision ?? "NONE";
}

export async function getLatestReviewSummaryMap(targets: ReviewTarget[]) {
    const uniqueTargets = Array.from(
        new Map(
            targets.map((target) => [buildReviewCompositeKey(target), target]),
        ).values(),
    );

    if (!uniqueTargets.length) {
        return new Map<string, ReviewSummaryGroup>();
    }

    const reviews = await prisma.review.findMany({
        where: {
            OR: uniqueTargets.map((target) => ({
                projectId: target.projectId,
                datasourceId: target.datasourceId,
                externalRecordId: target.externalRecordId,
            })),
        },
        orderBy: [{ updatedAt: "desc" }],
        select: {
            projectId: true,
            datasourceId: true,
            externalRecordId: true,
            decision: true,
            comment: true,
            suggestions: true,
            updatedAt: true,
            reviewer: {
                select: {
                    name: true,
                },
            },
        },
    });

    const summaryMap = new Map<string, ReviewSummaryGroup>();

    for (const review of reviews) {
        const key = buildReviewCompositeKey({
            projectId: review.projectId,
            datasourceId: review.datasourceId,
            externalRecordId: review.externalRecordId,
        });
        const current = summaryMap.get(key) ?? {
            latestReview: null,
            aiReview: null,
            manualReview: null,
        };
        const summary: ReviewSummary = {
            decision: review.decision,
            comment: review.comment,
            updatedAt: review.updatedAt.toISOString(),
            reviewerName: review.reviewer.name ?? "",
        };

        if (!current.latestReview) {
            current.latestReview = summary;
        }

        if (isAiGeneratedReview(review.suggestions)) {
            if (!current.aiReview) {
                current.aiReview = summary;
            }
        } else if (!current.manualReview) {
            current.manualReview = summary;
        }

        summaryMap.set(key, current);
    }

    return summaryMap;
}
